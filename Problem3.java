//Famous algorithm-Floyds algorithm-LinkedList cycle. It can be seen in circular arrays
//approach- using two pointers approach- Keep iterating both slow and fast pointers.
//when slow and fast pointers reaches at an index, we have a cycle

public class Solution {
    public ListNode detectCycle(ListNode head) {
        if(head == null || head.next == null) return null;
        ListNode slow = head;
        ListNode fast = head;
        boolean flag = false;
        while(fast != null && fast.next != null){
            slow = slow.next;
            fast = fast.next.next;
            if(slow == fast){
                flag = true;
                break;
            }
        }
        if(!flag) return null;
        slow = head;
        while(slow != fast){
            slow = slow.next;
            fast = fast.next;
        }
        return slow;
    }
}